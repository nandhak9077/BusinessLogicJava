class Printnum 
{
	public static void main(String[] args) 
	{
		for(int i = 1; i <= 200; i++)
		{
			System.out.print(" "+i);
		}
		
		System.out.println("");
		for(int i = 1; i <= 200; i+=2)
		{
			System.out.print(" "+i);
		}
		
        System.out.println("");
		for(int i = 0; i <= 200; i+=2)
		{
			System.out.print(" "+i);
		}
		
		
	}
}
